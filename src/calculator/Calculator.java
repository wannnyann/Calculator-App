package calculator;

public class Calculator {

    public int add(int a, int b){
        return a + b;
    }

    public int subtract(int a, int b){
        return  a - b;
    }

    public int multiply(int a, int b){
        return a * b;
    }

    public int div(int a, int b){
        return  a / b;
    }
    //余りはmodulo(モジュロ)
    public int mod(int a, int b){
        return a % b;
    }
}
